# Analizar los siguientes bloques de código. ¿Cuál será el resultado de ejecutarlos?
for i in range(5):
    """
        Se muestran los cuadrados de los primeros 5 numeros desde el 0 al 4
    """
    print(i * i)

for i in range(2, 6):
    """
        Se mostraran cuatro pares de números, el primero que va desde el 2 al 5, y el segundo que se forma elevando el 2 a un número que va desde el 2 al 5. 
    """
    print(i, 2 ** i)